    for n, m in pairs(v:GetChildren()) do
        local var = {}
        for b1, b2 in pairs(m:GetDescendants()) do
            if
                b2:IsA("ValueBase") and string.find(b2.Parent.Name, "Cannon") == nil and
                    string.find(b2.Parent.Name, "ColorMod") == nil and
                    b2.Name ~= "Ammo"
             then
                if v.Name ~= "Cargo" then
                    var[b2.Name] = b2.Value
                else
                    var["MaxCargo"] = b2.MaxValue
                end
            end
        end
        if v:FindFirstChild("Cost") == nil then
            var["Cost"] = 0
        end
        if v:FindFirstChild("Ammo", true) then
            var["MaxAmmo"] = v:FindFirstChild("Ammo", true).MaxValue
        end
        if m:FindFirstChild("RP_Cannons") then
            local cans = {}
            for c1, c2 in pairs(m["RP_Cannons"]:GetChildren()) do
                local c5 = {}
                for c3, c4 in pairs(c2:GetChildren()) do
                    if c4:IsA("ValueBase" ) then
                        c5[c4.Name] = c4.Value
                    end
                end
                cans[c2.Name] = c5
            end
            var["Cannons"] = cans
        end
        b[m.Name] = var
    end
    a[v.Name] = b
end
