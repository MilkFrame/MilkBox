--- cleaning and sorting raw tables (combine them all beforehand)
local fanartRaw = {}
function r(d)
    local s = {}
    for i = #d, 1, -1 do
        local t = d[i]
        local r = t[1]
        if s[r] then
            table.remove(d, i)
        else
            s[r] = true
        end
    end
end
function f(e)
    local o = "{"
    for n, v in pairs(e) do
        if type(n) == "string" then
            o = o .. '["' .. n .. '"]='
        end
        if type(v) == "table" then
            o = o .. f(v)
        elseif type(v) == "boolean" then
            o = o .. tostring(v)
        else
            o = o .. '"' .. v .. '"'
        end
        o = o .. ","
    end
    if o ~= "" then
        o = o:sub(1, o:len() - 1)
    end
    return o .. "}"
end
function e(r)
    local n = tonumber(r:match("%d+"))
    return n
end

function s(d)
    table.sort(d,function(a,b)
                local nA=e(a[1])
                local nB=e(b[1])
                if nA~=nil and nB~=nil then
                return nA<nB end end) end
